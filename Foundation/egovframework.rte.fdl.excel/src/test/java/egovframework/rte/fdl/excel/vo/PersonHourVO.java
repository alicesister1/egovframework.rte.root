package egovframework.rte.fdl.excel.vo;

public class PersonHourVO {
	
	private String name;
    private String id;
    private Double mon;
    private Double tue;
    private Double wed;
    private Double thu;
    private Double fri;
    private Double sat;
    private Double sun;
    
    public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Double getMon() {
		return mon;
	}
	public void setMon(Double mon) {
		this.mon = mon;
	}
	public Double getTue() {
		return tue;
	}
	public void setTue(Double tue) {
		this.tue = tue;
	}
	public Double getWed() {
		return wed;
	}
	public void setWed(Double wed) {
		this.wed = wed;
	}
	public Double getThu() {
		return thu;
	}
	public void setThu(Double thu) {
		this.thu = thu;
	}
	public Double getFri() {
		return fri;
	}
	public void setFri(Double fri) {
		this.fri = fri;
	}
	public Double getSat() {
		return sat;
	}
	public void setSat(Double sat) {
		this.sat = sat;
	}
	public Double getSun() {
		return sun;
	}
	public void setSun(Double sun) {
		this.sun = sun;
	}
}